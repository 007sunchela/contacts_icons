import 'package:flutter/material.dart';
import 'package:font_awesome_flutter/font_awesome_flutter.dart';

// VK
class VKIcon extends StatelessWidget {
  final double size;
  final Color color;

  const VKIcon({Key? key, this.size = 30.0, this.color = Colors.blue})
      : super(key: key);

  @override
  Widget build(BuildContext context) {
    return FaIcon(FontAwesomeIcons.vk, size: size, color: color);
  }
}

